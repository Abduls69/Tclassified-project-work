# A Set is a collection which is unordered and unindexed. No duplicate numbers

# Create a set
colours_set = {'red','yellow','green','white'}

print(colours_set)

# Check if in set

print('black' in colours_set)

# Add to set
colours_set.add('pink')
print(colours_set)

# Remove to set
colours_set.remove('pink')
print(colours_set)

# Add duplicate to set
colours_set.add('yellow')
print(colours_set)

#Clear set
colours_set.clear()
print(colours_set)

# Delete set
del colours_set
print(colours_set)






